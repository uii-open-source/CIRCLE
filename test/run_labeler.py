import argparse
import os

import pandas as pd
import torch
from transformers import AutoModelForCausalLM, AutoTokenizer


# Default prompt template for disease label extraction; {report_text} will be filled at runtime
DEFAULT_PROMPT_TEMPLATE = "请提取报告中的疾病标签。报告为：{report_text}"


def load_labeler_model(model_name, gpu_id=0):
    """
    Load tokenizer and causal language model for report label extraction.
    Args:
        model_name (str): Local or remote model path.
        gpu_id (int): GPU id used for inference when CUDA is available.
    Returns:
        tuple: (tokenizer, model, device)
    """
    # Select device: use specified GPU when CUDA is available, otherwise fall back to CPU
    if torch.cuda.is_available():
        device = f"cuda:{gpu_id}"
        device_map = {"": device}
    else:
        device = "cpu"
        device_map = "auto"

    # Load tokenizer and model weights
    tokenizer = AutoTokenizer.from_pretrained(model_name)
    model = AutoModelForCausalLM.from_pretrained(
        model_name,
        torch_dtype="auto",
        device_map=device_map,
    )
    return tokenizer, model, device


def extract_labels(tokenizer, model, report_text):
    """
    Extract disease labels from one diagnostic report text.
    Args:
        tokenizer: HuggingFace tokenizer.
        model: HuggingFace CausalLM model.
        report_text (str): Input report text.
    Returns:
        str: Extracted label text generated by the model.
    """
    # Format prompt and wrap as a single-turn chat message
    prompt = DEFAULT_PROMPT_TEMPLATE.format(report_text=report_text)
    messages = [{"role": "user", "content": prompt}]

    # Apply chat template to produce the model input string
    text = tokenizer.apply_chat_template(
        messages,
        tokenize=False,
        add_generation_prompt=True,
        enable_thinking=False,
    )
    model_inputs = tokenizer([text], return_tensors="pt").to(model.device)

    # Run autoregressive generation
    generated_ids = model.generate(
        **model_inputs,
        max_new_tokens=512,
    )
    # Slice off the prompt tokens to get only the newly generated tokens
    output_ids = generated_ids[0][len(model_inputs.input_ids[0]):]
    content = tokenizer.decode(output_ids, skip_special_tokens=True).strip()

    # Strip chain-of-thought reasoning block if present
    if "</think>" in content:
        content = content.split("</think>")[-1].strip()
    return content


def run_labeler(
    model_name,
    report_text_csv,
    output_csv,
    gpu_id=0,
):
    """
    Run report label extraction on all records in a CSV and save results.
    Args:
        model_name (str): Labeler model path.
        report_text_csv (str): CSV path containing a `report` column.
        output_csv (str): Output CSV path; result contains `report` and `label` columns.
        gpu_id (int): GPU id for inference.
    """
    # Load labeler model and tokenizer
    tokenizer, model, device = load_labeler_model(model_name, gpu_id=gpu_id)
    print(f"use device: {device}")

    # Read input CSV and validate required column
    report_df = pd.read_csv(report_text_csv)
    if "report" not in report_df.columns:
        raise ValueError("report_text_csv must contain column: report")

    # Iterate over all reports and extract labels one by one
    output_records = []
    for report_text in report_df["report"].fillna("").astype(str).tolist():
        label_text = extract_labels(
            tokenizer=tokenizer,
            model=model,
            report_text=report_text,
        )
        output_records.append({
            "report": report_text,
            "label": label_text,
        })

    # Save results to output CSV
    os.makedirs(os.path.dirname(output_csv) or ".", exist_ok=True)
    pd.DataFrame(output_records, columns=["report", "label"]).to_csv(
        output_csv,
        index=False,
        encoding="utf-8-sig",
    )
    print(f"save done: {output_csv}")


if __name__ == '__main__':
    parser = argparse.ArgumentParser(description='infer report label extraction')
    parser.add_argument(
        '--model_name',
        default='/model/CIRCLE-Labeler',
    )
    parser.add_argument('--gpu_id', type=int, default=0)
    parser.add_argument('--report_text_csv', default='data/report_text_test.csv')
    parser.add_argument('--output_csv', default='output/label.csv')
    args = parser.parse_args()

    run_labeler(
        model_name=args.model_name,
        report_text_csv=args.report_text_csv,
        output_csv=args.output_csv,
        gpu_id=args.gpu_id,
    )